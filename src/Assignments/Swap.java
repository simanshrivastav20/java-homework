package Assignments;

public class Swap {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        int c;
        c= a + b;
        a= c - a;
        b= c - b;
        System.out.println("swap:"+"a ="+ a +"b = "+ b);



        int x = 10;
        int y = 5;
        x = x + y;
        y = x - y;
        x = x - y;
        System.out.println("SWAP:"
                + " x = " + x + "y = " + y);


    }
}
